-- ────────────────────────────────────────────────────────────────
-- PART 2: Seeds Management — scheduled seeds-in
-- ────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS nops_seed_schedules (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  breed           TEXT NOT NULL,
  supplier        TEXT,
  scheduled_date  DATE NOT NULL,
  scheduled_qty   INT  NOT NULL CHECK (scheduled_qty > 0),
  remark          TEXT,
  status          TEXT NOT NULL DEFAULT 'scheduled'
                  CHECK (status IN ('scheduled','partial','received','cancelled')),
  created_by      TEXT,
  created_at      TIMESTAMPTZ DEFAULT now()
);

CREATE INDEX IF NOT EXISTS nops_seed_schedules_date_idx
  ON nops_seed_schedules (scheduled_date);

