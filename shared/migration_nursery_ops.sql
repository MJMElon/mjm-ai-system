-- ────────────────────────────────────────────────────────────────
-- PART 3: Seeds Management — actual receives (per schedule)
-- ────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS nops_seed_receipts (
  id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  schedule_id    BIGINT NOT NULL REFERENCES nops_seed_schedules(id) ON DELETE CASCADE,
  received_date  DATE NOT NULL,
  qty            INT  NOT NULL CHECK (qty > 0),
  remark         TEXT,
  received_by    TEXT,
  created_at     TIMESTAMPTZ DEFAULT now()
);

CREATE INDEX IF NOT EXISTS nops_seed_receipts_schedule_idx
