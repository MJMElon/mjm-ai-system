-- New module: NURSERY OPERATION MANAGEMENT (nops_*)
--   1. nops_work_types      — Motion Study: the work types a plot goes
--                             through (Soil Transport, Polybag Filling, …)
--                             with the standard days / crew each one needs.
--                             Fully editable — add & remove from the module's
--                             Settings page, nothing is hard-coded.
--   2. nops_seed_schedules  — Seeds Management: scheduled seeds-in.
--   3. nops_seed_receipts   — Seeds Management: actual receives against a
--                             schedule (partial deliveries supported).
--   4. nops_plot_works      — one row per piece of plot work. Powers BOTH
--                             the Monthly Schedule page (preset this / next
--                             month, history of what is done) AND the Life
--                             of Plot page (everything ever done on a plot).
--                             Plots come from the existing shared_plots —
--                             the same plots managed in the Nursery Seedling
--                             Stock Management settings.
--
-- Module access: permissions.modules.nursery_ops = 'admin'|'normal'|'none',
-- granted on the main portal's User Access (JSONB — no DDL needed).
--
-- Run in Supabase SQL Editor (main project: kibqjztozokohqmhqqqf).
-- ============================================================================

-- ────────────────────────────────────────────────────────────────
-- PART 1: Motion Study work types
-- ────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS nops_work_types (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        TEXT NOT NULL UNIQUE,
  sort_order  INT  NOT NULL DEFAULT 0,
  std_days    NUMERIC,      -- usual number of days this work takes for one plot
  std_crew    INT,          -- usual number of workers needed
  remark      TEXT,         -- motion-study notes (method, tools, pace…)
  created_at  TIMESTAMPTZ DEFAULT now()
);

ALTER TABLE nops_work_types ENABLE ROW LEVEL SECURITY;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
     WHERE schemaname='public' AND tablename='nops_work_types'
       AND policyname='Authenticated read work types'
  ) THEN
    CREATE POLICY "Authenticated read work types" ON nops_work_types
      FOR SELECT TO authenticated USING (true);
  END IF;
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
     WHERE schemaname='public' AND tablename='nops_work_types'
       AND policyname='Authenticated write work types'
  ) THEN
    CREATE POLICY "Authenticated write work types" ON nops_work_types
      FOR ALL TO authenticated USING (true) WITH CHECK (true);
  END IF;
END $$;

