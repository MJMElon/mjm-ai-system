-- ────────────────────────────────────────────────────────────────
-- PART 4: Plot works — Monthly Schedule + Life of Plot
-- ────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS nops_plot_works (
  id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  month          TEXT NOT NULL,     -- 'YYYY-MM' schedule bucket
  nursery_name   TEXT,
  plot_name      TEXT NOT NULL,     -- from shared_plots (same plots as the
                                    -- Seedling Stock Management settings)
  work_type_id   BIGINT REFERENCES nops_work_types(id) ON DELETE SET NULL,
  work_type      TEXT NOT NULL,     -- name snapshot — history survives even
                                    -- if the work type is later removed
  planned_start  DATE,
  planned_days   NUMERIC,           -- prefilled from the motion study std_days
  status         TEXT NOT NULL DEFAULT 'planned'
                 CHECK (status IN ('planned','in_progress','done','cancelled')),
  actual_start   DATE,
  actual_end     DATE,
  done_by        TEXT,
  remark         TEXT,
  created_by     TEXT,
  created_at     TIMESTAMPTZ DEFAULT now()
);

CREATE INDEX IF NOT EXISTS nops_plot_works_month_idx ON nops_plot_works (month);
CREATE INDEX IF NOT EXISTS nops_plot_works_plot_idx  ON nops_plot_works (plot_name);

